class loops
{
public static void main(String[] args)
{
int a=10;
for(int i=0;i<=10;i++)
{
if(i==a)
{
System.out.println(i+ " equals to" + a);
}
else{
System.out.println(i+ " not equals to" +a);
}
}
}
}
